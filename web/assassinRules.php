<h3 text-align="center" font-family="Courier New">Overall Rules:</h3>
<ul>
    <li class = "rules">Make an appearance somewhere in Hanover once a day 
        (If you are gone for more than 24 hours, a bounty will be put on you)</li>
    <li class = "rules">You must leave your house at least once a day </li>
    <li class = "rules">The only weapons allowed are water guns → FILLED WITH WATER ONLY </li>
    <li class = "rules">No shooting while driving/when your target is driving (a parked car is fair game)</li>
    <li class = "rules">Snap maps are strongly encouraged (and may be made mandatory later on in the game)</li>
    <li class = "rules">You may recruit others to help you make your assassination. The people helping you 
        may not make physical contact with the target, and the actual "kill shot" must come
         from the assassin, not anyone helping. </li>
    <li class = "rules">When someone is assassinated, you must submit a picture/video within 2 HOURS, or IT DOES NOT COUNT </li>
    <li class = "rules">If the target shoots the assassin before they are killed, the target will receive 1 hour of safety. If 
        there is a disagreement, settle it with rock-paper-scissors (best 2 out of 3) and submit a video</li>
    <li class = "rules">SAFETY FIRST</li>
    <li class = "rules">Tell your family/neighbors! We do not want the police involved </li>
</ul>
