<h3 text-align="center" font-family="Courier New">Safe Zones:</h3>
<ul>
    <li class = "rules">School-sanctioned events are OFF LIMITS </li>
    <li class = "rules">Sports practices, banquets, and games are OFF LIMITS </li>
    <li class = "rules">The hour before an AP Exam is OFF LIMITS (players will 
        have 10 minutes of safety immediately after their exam)</li>
    <li class = "rules">Targets have 10 minutes of safety after school/ 10 mins
         before practice </li>
    <li class = "rules">Workplaces are off-limits (you are allowed to get your 
        target leaving work, but not when arriving at work or during their shift)</li>
    <li class = "rules">Do not enter your target’s home unless invited inside </li>
    <li class = "rules">Places of worship are off-limits </li>
    <li class = "rules">You may shoot through open windows but DO NOT break into someone's house.
        That's rude.</li>
    <li class = "rules">If you are someone's property and someone asks you to leave, respect it. You must leave. </li>
</ul>
